function generateTableToDOM(table, container) {
    var $table = $('<table>');

    // Adiciona atributos da tabela
    if (table.tableId) $table.attr('id', table.tableId);
    if (table.classes) $table.addClass(table.classes);
    if (table.customData) $table.attr(table.customData);
    if (table.style) $table.attr('style', table.style);

    // Cria e adiciona o cabeçalho
    if (table.header && table.header.rows.length) {
        var $thead = $('<thead>');
        table.header.rows.forEach(function (headerRow) {
            var $tr = $('<tr>');

            if (headerRow.style) $tr.attr('style', headerRow.style);
            if (headerRow.rowId) $tr.attr('id', headerRow.rowId);
            if (headerRow.classes) $tr.addClass(headerRow.classes);
            if (headerRow.customData) $tr.attr(headerRow.customData);

            headerRow.cols.forEach(function (col) {
                var $th = $('<th>').html(col.content);

                if (col.style) $th.attr('style', col.style);
                if (col.colId) $th.attr('id', col.colId);
                if (col.classes) $th.addClass(col.classes);
                if (col.customData) $th.attr(col.customData);

                $tr.append($th);
            });
            $thead.append($tr);
        });
        $table.append($thead);
    }

    // Cria e adiciona o corpo da tabela
    var $tbody = $('<tbody>');
    if (table.body.customData) $tbody.attr(table.body.customData);
    $table.append($tbody);

    // Adiciona a tabela ao container
    $(container).append($table);

    // Adiciona as linhas do corpo uma por uma
    if (table.body && table.body.rows.length) {
        table.body.rows.forEach(function (row) {
            var $tr = $('<tr>');

            if (row.style) $tr.attr('style', row.style);
            if (row.rowId) $tr.attr('id', row.rowId);
            if (row.classes) $tr.addClass(row.classes);
            if (row.customData) $tr.attr(row.customData);

            row.cols.forEach(function (col) {
                var $td = $('<td>').html(col.content);

                if (col.style) $td.attr('style', col.style);
                if (col.colId) $td.attr('id', col.colId);
                if (col.classes) $td.addClass(col.classes);
                if (col.customData) $td.attr(col.customData);

                $tr.append($td);
            });
            $tbody.append($tr);
        });
    }
}


