function getInstrucao(instrucaostamp) {

    $.getScript("https://cdn.jsdelivr.net/npm/driver.js@1.0.1/dist/driver.js.iife.js", function () {

        driver = window.driver.js.driver;
        var elementosMonitorados = []
        //console.log("Instrução carregada", instrucaostamp);
        var dadosInstrucao = instrucoes.find(function (obj) {
            return obj.instrucao.intustamp == instrucaostamp
        });
        var steps = [{
            popover: {
                title: dadosInstrucao.instrucao.nome,
                description: dadosInstrucao.instrucao.ajuda,
                showButtons: ["close", "next"]
            }
        }];

        dadosInstrucao.linhasInstrucao.sort(function (a, b) {

            return a.ordem - b.ordem
        });
        for (var i = 0; i < dadosInstrucao.linhasInstrucao.length; i++) {
            var linhaInstrucao = dadosInstrucao.linhasInstrucao[i]

            var tipoevento = linhaInstrucao.tipoevento
            if (tipoevento == "seminteracao") {

                var stepData = {
                    popover: {
                        title: linhaInstrucao.titulo,
                        description: linhaInstrucao.texto
                    }
                }

                if (linhaInstrucao.selector) {
                    stepData.element = linhaInstrucao.selector

                }
                steps.push(stepData)


            }

            if (tipoevento != "seminteracao") {


                var stepData = {
                    element: linhaInstrucao.selector,
                    popover: {
                        title: linhaInstrucao.titulo,
                        description: linhaInstrucao.texto,
                        showButtons: ["previous", "close"]
                    }
                }
                elementosMonitorados.push({ selector: linhaInstrucao.selectorevento, tipoevento: tipoevento })
                steps.push(stepData)

            }


        }
        var driverObj = driver({
            showProgress: true,
            allowClose: false,
            nextBtnText: 'Próximo',
            prevBtnText: 'Anterior',
            doneBtnText: 'Terminar',
            steps: steps
        });

        $("#modalInstrucoes").modal("hide")


        elementosMonitorados.forEach(function (elementoMonitorado) {

            //  $(document).off(elementoMonitorado.tipoevento, elementoMonitorado.selector);

            // Ligar o evento novamente
            if (elementoMonitorado.tipoevento == "changedate") {
                /*$(elementoMonitorado.selector).datepicker({
                    onSelect: function (dateText, inst) {
                        $(elementoMonitorado.selector).datepicker("hide");
                        driverObj.moveNext();

                    }
                });*/
            } else {


                $(document).on(elementoMonitorado.tipoevento, elementoMonitorado.selector, function () {
                    // verificarElementoMonitorado($(this).attr('id'));
                    driverObj.moveNext();
                });
            }


        })

        driverObj.drive();

        //  console.log("dadosInstrucao", steps)

    });
}


