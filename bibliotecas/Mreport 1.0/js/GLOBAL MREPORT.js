function handleHardReload(url) {
    // Verifica se a URL já contém o parâmetro 'hardref'
    if (!window.location.href.includes('hardref')) {
        // Adiciona o parâmetro 'hardref' à URL para evitar loops
        var separator = url.indexOf('?') !== -1 ? '&' : '?';
        var newUrl = url + separator + 'hardref=true';

        return fetch(newUrl, {
            headers: {
                Pragma: 'no-cache',
                Expires: '-1',
                'Cache-Control': 'no-cache',
            },
        }).then(function () {
            console.log("Hard refresh", newUrl);
            window.location.href = newUrl;
            // Isso garante que a página seja recarregada com URLs contendo '#'
            window.location.reload();
        });
    }
}


