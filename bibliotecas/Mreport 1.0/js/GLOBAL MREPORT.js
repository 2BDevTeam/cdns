function ackMessage(messageId) {

    if (!isConnected) {

        alertify.error("Erro de conexão ao enviar mensagem")
        console.log("Erro de conexão ao enviar mensagem pois a mesma está fechada")
        return
    }

    var message = {

        from: userMessageId,
        to: "",
        messageType: "SIMPLE_MESSAGE",
        messageId: messageId,
        displayMode: "DEFAULT",
        criticality: 3,
        operation: "ACK_MESSAGE",
        conversationId: generateUUID(),
        text: "",
        recordId: ""
    }

    messages = messages.filter(function (obj) {
        return obj.messageId == messageId
    });

    console.log("MESSAGE ACKED", messageId);
    socket.send(JSON.stringify(message));
}

