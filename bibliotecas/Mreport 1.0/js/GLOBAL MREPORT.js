function connect() {

    return false
    if (!userMessageId) {
        $.ajax({
            type: "POST",
            url: "../programs/gensel.aspx?cscript=getusermessageid",
            async: false,
            success: function (response) {

                var errorMessage = "ao trazer dados do utilizador para a conexão ao websocket"
                try {
                    console.log(response)
                    if (response.cod != "0000") {

                        console.log("Erro " + errorMessage, response)
                        return false
                    }
                    console.log("Getting user id")
                    userMessageId = response.data.userId;
                    dadosUserWebsocket = response.data;
                } catch (error) {
                    console.log("Erro interno " + errorMessage, response)
                    //alertify.error("Erro interno " + errorMessage, 10000)
                }

            }
        })
    }


    console.log("Connecting to WebSocket server with userId: " + userMessageId)
    socket = new WebSocket('ws://localhost:7134/ws?userId=' + userMessageId);

    socket.onopen = function (event) {
        console.log("Connected to WebSocket server.........")
        isConnected = true;
        //document.getElementById('messages').innerHTML += '<p>Connected to WebSocket server</p>';
    };

    socket.onmessage = function (event) {

        try {

            console.log('Message from server ', event.data);
            handleServerMessage(event.data)
        } catch (error) {
            console.log("Erro ao processar mensagem do servidor", error)
        }
        //document.getElementById('messages').innerHTML += '<p>Received: ' + event.data + '</p>';
    };

    socket.onclose = function (event) {
        isConnected = false;
        console.log("WebSocket connection closed")
        // document.getElementById('messages').innerHTML += '<p>WebSocket connection closed</p>';
        retryConnection();

    };

    socket.error = function (event) {
        //isConnected = false;
        //console.log("WebSocket connection closed")
        // document.getElementById('messages').innerHTML += '<p>WebSocket connection closed</p>';
        //retryConnection();

        console.log("Erro na conexão ao WebSocket server", event)


    };
}


function handleServerMessage(message) {

    var messageData = JSON.parse(message);

    if (!messageData.toAck) {
        return;
    }
    //console.log("Mensagem recebida", messageData)

    var messageInfo = messages.find(function (obj) {

        return obj.messageId == messageData.messageId
    });


    if (messageInfo) {

        console.log("Message already Exists")
        return
    }
    messages.push(messageData);

    var notyf = new Notyf({
        position: {
            x: 'right',
            y: 'top',
        },
        dismissible: true,
        ripple: true,
        duration: 0,
        types: [
            {
                type: 'warning',
                background: '#f79523',
                /*icon: {
                    className: 'fa fa-bell',
                    tagName: 'i',
                    text: '',
                    color: "white"
                }*/
            }
        ]
    })

    var htmlMessage = "<div class='' ><h5 style='font-weight:bold'><span>Mensagem de </span><span>" + messageData.senderName + "</span></h5></div>"
    //htmlMessage+= "<div class='row' ><h5 style='font-weight:bold'><span>"+messageData.senderEntity+"</span></h5></div>"
    //htmlMessage+= "<div class='row' ><h5 style='font-weight:bold'><span>"+messageData.subject+"</span></h5></div>"
    htmlMessage += "<div class='' ><p style=''><span>" + messageData.text.substring(0, 30) + "...</span></p></div>"
    htmlMessage += "<button onClick='verDetalhesMensagem(\"" + messageData.messageId + "\")' style='background:white;color:orange' class='btn btn-default-sm'>Ver mais...</button>"
    notyf.open({
        type: 'warning',
        message: htmlMessage
    }).on('dismiss', function (data) {
        ackMessage(messageData.messageId)
    });;


}

