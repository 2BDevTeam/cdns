function getDistinctWithKeys(arr, distinctKey) {
    var uniqueObjects = arr.reduce(function (accumulator, current) {
        if (!accumulator.some(function (obj) {
            return obj[distinctKey] === current[distinctKey];
        })) {
            accumulator.push(current);
        }
        return accumulator;
    }, []);

    return uniqueObjects;
}
