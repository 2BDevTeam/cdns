function orderArray(indexesOrdering, arr) {
    ////console.log("indexg ordering",indexesOrdering)
    for (var i = 0; i < indexesOrdering.length; i++) {

        var fromIndex = arr.findIndex(function (element) {
            return element[indexesOrdering[i].fieldKey] == indexesOrdering[i].fieldValue;
        });

        if (fromIndex >= 0) {
            var toIndex = indexesOrdering[i].order;
            var element = arr.splice(fromIndex, 1)[0];
            arr.splice(toIndex, 0, element);
        }
    }


    return arr
}
